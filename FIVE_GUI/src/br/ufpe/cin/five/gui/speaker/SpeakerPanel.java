    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        logger.info("Botão btRemove pressionado.");
        if (JOptionPane.showConfirmDialog(null, "Deseja excluir o locutor? Esta ação excluirá todos as mostras de áudio do locutor selecionado!", "Excluir locutor", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            logger.info("Usuário confirmou a exclusao");
            int line = jTable1.getSelectedRow();
            if (line >= 0) {
                try {
                    int id = Integer.parseInt(jTable1.getValueAt(line, 0).toString());
                    Speaker speaker = facade.searchSpeaker(id);                
                    
                    SampleFilter filter = new SampleFilter();
                    filter.getSpeakers().add(speaker);
                    
                    if (facade.searchSample(filter).isEmpty()) {
                        facade.removeSpeaker(speaker);
                        removeLine(line, jTable1);
                        logger.info("Locutor excluído com sucesso!");
                        JOptionPane.showMessageDialog(null, "Locutor excluído com sucesso!");
                    } else {
                        logger.warn("Existem amostras de áudio relacionadas a esse locutor!");
                        JOptionPane.showMessageDialog(null, "Existem amostras áudio relacionadas a essa locutor!");
                    }
                } catch (Exception ex) {
                    logger.error(ex.getMessage());
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
}//GEN-LAST:event_btRemoveActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        logger.info("Botão btUpdate pressionado.");
        int line = jTable1.getSelectedRow();
        if (line >= 0) {
            try {
                int id = Integer.parseInt(jTable1.getValueAt(line, 0).toString());
                Speaker speaker = facade.searchSpeaker(id);                     
                SpeakerDialog dialog = new SpeakerDialog(null, true, speaker, 1);
                dialog.pack();
                dialog.setLocationRelativeTo(this);
                dialog.setVisible(true);
                removeLine(line, jTable1);
                addAllLinesTableSpeaker();
            } catch (Exception ex) {
                logger.error(ex.getMessage());
                JOptionPane.showMessageDialog(null, ex.getMessage());           
            }
        }
}//GEN-LAST:event_btUpdateActionPerformed

