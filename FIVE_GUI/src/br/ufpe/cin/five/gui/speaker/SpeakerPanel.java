    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        logger.info("Botão btSearch pressionado.");
        String text = "";
        if (!tfLogin.getText().equals("") && !tfName.getText().equals("")) {
            JOptionPane.showMessageDialog(jPanel5, "Escolha apenas um dos campos para pesquisar", "Pesquisar Locutor", selectedLine);
        } else {
            if (!tfLogin.getText().equals("")) {
                text = tfLogin.getText();
            }
            if (!tfName.getText().equals("")) {
                text = tfName.getText();
            }
            if (text.length() == 0) {
                sorter.setRowFilter(null);
            } else {
                sorter.setRowFilter(RowFilter.regexFilter(text));
            }
        }
}//GEN-LAST:event_btSearchActionPerformed

    protected void addLineTableSpeaker(Speaker speaker) {
        model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{speaker.getId(), speaker.getName()});
    }

    public void addAllLinesTableSpeaker() {
        removeAllLinesTableSpeaker();
        for (Speaker speaker : facade.getProject().getSpeakers()) {
            addLineTableSpeaker(speaker);
        }        
    }

    protected void updateLineTableSpeaker(String speaker, int line) {
        model = (DefaultTableModel) jTable1.getModel();
        model.setValueAt(speaker, line, 0);
    }

    private void removeAllLinesTableSpeaker() {
        model = (DefaultTableModel) jTable1.getModel();
        while (model.getRowCount() >= 1) {
            model.removeRow(0);
        }
    }

    private void removeLine(int line, JTable table) {
        model = (DefaultTableModel) table.getModel();
        model.removeRow(line);
    }
