    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        try {
            logger.info("Botão OK pressionado");

            if (validateProjectAttributes()) {

                Project project = facade.getProject();
                if (project != null){
                    project.setName(pnl.getProjectName());
                    project.setDirectory(pnl.getPath() + File.separator + pnl.getProjectName());
                    project.setDbLogin(pdb.txtLoginDB.getText());
                    project.setDbPassword(new String(pdb.txtPasswordDB.getPassword()));
                    project.setDbDriver(pdb.getDriverType());
                    project.setDbUrl(DriverUtils.getConnectionURL(pdb.txtUrlDB.getText(),project.getDbDriver())+"/"+project.getName());                    
                    facade.setProject(project);
                    logger.info("Pasta do projeto selecionada foi "+pnl.getPath());
                }

                facade.createProject(facade.getProject());
                facade.initProjectRegisters();

                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível criar o projeto. Verifique se todas as informações necessárias foram fornecidas.");
                logger.warn("Não foi possível criar o projeto. Verifique se todas as informações necessárias foram fornecidas.");
            }

        } catch (RegisterException ex) {
            logger.error("", ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } 
    }//GEN-LAST:event_btOkActionPerformed

    private boolean validateProjectAttributes() {
        boolean validation = true;
        if (facade.getProject() == null) {
            validation = false;                        
        }
        validation = pdb.validateDataBase();        
        return validation;
    }

    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        if(!pressedButton)
        {
            if (jTabbedPane1.getSelectedIndex() == 0) {
                voltar();
            } else {
                avancar();
            }
        }else
        {
            pressedButton = false;
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged
