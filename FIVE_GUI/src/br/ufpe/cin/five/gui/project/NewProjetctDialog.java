    private void avancar() {
        logger.info("Botao avancar pressionado.");
        pressedButton = true;
        if(jLabel3.getForeground().equals(Color.blue))
        {
            if (Facade.getInstance().getProject() != null) {
                jLabel3.setForeground(Color.black);
                jLabel4.setForeground(Color.blue);
                btVoltar.setEnabled(true);                                
                jTabbedPane1.setSelectedIndex(1);
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um projeto.");
                jTabbedPane1.setSelectedIndex(0);
            }
        }else if(jLabel4.getForeground().equals(Color.blue))
        {
            if(pnl.tfProjectName.getText().isEmpty() || pnl.tfProjectPath.getText().isEmpty())
            {
            JOptionPane.showMessageDialog(null, "Selecione um nome e local para o projeto.");
                jTabbedPane1.setSelectedIndex(1);
            }else
            {
                jLabel3.setForeground(Color.black);
                jLabel4.setForeground(Color.black);
                jLabel1.setForeground(Color.blue);
                btAvancar.setEnabled(false);
                btVoltar.setEnabled(true);
                btOk.setEnabled(true);
                jTabbedPane1.setSelectedIndex(2);
            }
        }
    }

    private void voltar() {
        logger.info("Botao voltar pressionado.");
        pressedButton = true;
        if(jLabel4.getForeground().equals(Color.blue))
        {
            jLabel3.setForeground(Color.blue);
            jLabel4.setForeground(Color.black);
            btVoltar.setEnabled(false);
            btAvancar.setEnabled(true);
            btOk.setEnabled(false);
            jTabbedPane1.setSelectedIndex(0);
        }else if (jLabel1.getForeground().equals(Color.blue))
        {
            jLabel1.setForeground(Color.black);
            jLabel4.setForeground(Color.blue);
            btVoltar.setEnabled(true);
            btAvancar.setEnabled(true);
            btOk.setEnabled(false);
            jTabbedPane1.setSelectedIndex(1);
        }
        
    }

