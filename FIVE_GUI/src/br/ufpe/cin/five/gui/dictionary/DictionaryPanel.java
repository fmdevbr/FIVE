    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        logger.info("O botão btUpdate foi pressionado.");
        int line = jTable1.getSelectedRow();
        if (line >= 0) {
            String word = (String) jTable1.getValueAt(line, 0);
            String phoneticRepresentation = (String) jTable1.getValueAt(line, 1);
            logger.info("Abrindo Word Dialog para alteracao do registro.");
            DictionaryDialog dialog = new DictionaryDialog(null, true, word, phoneticRepresentation, 1);
            dialog.pack();
            dialog.setLocationRelativeTo(this);
            dialog.setVisible(true);
            removeLine(line, jTable1);
            addAllLinesTableWord();
        }
}//GEN-LAST:event_btUpdateActionPerformed

    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        logger.info("O botão btRemove foi pressionado.");
        if (JOptionPane.showConfirmDialog(null, "Deseja excluir a representação fonética?", "Excluir representação fonética", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            logger.info("O usuario confirmou a exclusão.");
            int line = jTable1.getSelectedRow();
            if (line >= 0) {
                try {
                    String wordDescription = (String) jTable1.getValueAt(line, 0);
                    String phoneticRepresentation = (String) jTable1.getValueAt(line, 1);
                    Facade.getInstance().removePhoneticRepresentation(wordDescription, phoneticRepresentation);
                    removeLine(line, jTable1);
                    logger.info("Representacao fonética excluída com sucesso!");
                    JOptionPane.showMessageDialog(null, "Representação fonética excluída com sucesso!", "Excluir representação fonétic", JOptionPane.INFORMATION_MESSAGE);
                } catch (RegisterException ex) {
                    logger.error(ex);
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
}//GEN-LAST:event_btRemoveActionPerformed

    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        logger.info("Botão btSearch pressionado.");
        String text = "";
        if (!tfWord.getText().equals("") && !tfPhoneticRepresentation.getText().equals("")) {
            JOptionPane.showMessageDialog(jPanel5, "Escolha apenas um dos campos para pesquisar", "Pesquisar Representação Fonética", selectedLine);
        } else {
            if (!tfWord.getText().equals("")) {
                text = tfWord.getText();
            }
            if (!tfPhoneticRepresentation.getText().equals("")) {
                text = tfPhoneticRepresentation.getText();
            }
            if (text.length() == 0) {
                sorter.setRowFilter(null);
            } else {
                sorter.setRowFilter(RowFilter.regexFilter(text));
            }
        }
}//GEN-LAST:event_btSearchActionPerformed

