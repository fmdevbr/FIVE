    protected void addLineTableWord(String word, String phoneticRepresentation) {
        model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new String[]{word, phoneticRepresentation});
    }

    public void addAllLinesTableWord() {
        removeAllLinesTableWord();
        List<String> reps = new ArrayList<String>();
        List<Utterance> utterances = Facade.getInstance().getProject().getUtterances();
        for (Utterance utterance : utterances) {
            List<Phrase> phrases = utterance.getPhrases();
            for (Phrase phrase : phrases) {
                List<Word> words = phrase.getWords();
                for (Word word : words) {
                    List<String> representations = word.getPhoneticRepresentations();
                    for (String representation : representations) {
                        if (!reps.contains(representation.trim())) {
                            reps.add(representation);
                            addLineTableWord(word.getDescription(), representation);
                        }
                    }
                }
            }
        }
    }

    protected void updateLineTableWord(String sentence, int line) {
        model = (DefaultTableModel) jTable1.getModel();
        model.setValueAt(sentence, line, 0);
    }

    private void removeAllLinesTableWord() {
        model = (DefaultTableModel) jTable1.getModel();
        while (model.getRowCount() >= 1) {
            model.removeRow(0);
        }
    }

    private void removeLine(int line, JTable table) {
        model = (DefaultTableModel) table.getModel();
        model.removeRow(line);
    }
