    protected void addLineTable(WordException word) {
                
       DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
       model.addRow(new Object[]{word.getException(),word.getRepresentation()});
             
    }
    
    private void removeAllLinesTable() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        while (model.getRowCount() >= 1) {
            model.removeRow(0);
        }
    }
    
    public void populateTable()
    {
        removeAllLinesTable();
        WordExceptionRegister register = new WordExceptionRegister();
        words = register.getWords();
         for(WordException word : words)            
            addLineTable(word);        
    }

