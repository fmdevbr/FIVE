    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int line = jTable2.getSelectedRow();
        if (line >= 0) {
           String word = jTable2.getValueAt(line, 0).toString().toString();               
            for(WordException w : words)                
                if(w.getException().equals(word))
                    new EditWordExceptionDialog(new JFrame(), true, this, w, words).setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int line = jTable2.getSelectedRow();
        if (line >= 0) {
           String word = jTable2.getValueAt(line, 0).toString().toString();        
           WordException wordToRemove = null;
            for(WordException w : words)                
                if(w.getException().equals(word))
                    wordToRemove = w;
            
            words.remove(wordToRemove);
            
            WordExceptionRegister register = new WordExceptionRegister();
            
            register.saveWords(words);
            
            populateTable();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

