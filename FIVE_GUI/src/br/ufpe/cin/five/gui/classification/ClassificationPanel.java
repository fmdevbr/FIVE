    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        logger.info("Botão btSearch pressionado.");
        String text = "";
        if (!tfTechnique.getText().equals("") && !tfDescription.getText().equals("")) {
            JOptionPane.showMessageDialog(jPanel4, "Escolha apenas um dos campos para pesquisar", "Pesquisar Classificação", selectedLine);
        } else {
            if (!tfTechnique.getText().equals("")) {
                text = tfTechnique.getText();
            }
            if (!tfDescription.getText().equals("")) {
                text = tfDescription.getText();
            }
            if (text.length() == 0) {
                sorter.setRowFilter(null);
            } else {
                sorter.setRowFilter(RowFilter.regexFilter(text));
            }
        }
    }//GEN-LAST:event_btSearchActionPerformed

    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        logger.info("O botão btRemove foi pressionado.");
        if (JOptionPane.showConfirmDialog(null, "Deseja excluir a classificação?", "Excluir classificação", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            logger.info("Usuário confirmou a exclusao.");
            int line = jTable1.getSelectedRow();
            if (line >= 0) {
                try {
                    int id = Integer.parseInt(jTable1.getValueAt(line, 0).toString());
                    Classification classification = facade.searchClassification(id);
                    
                    facade.removeClassification(classification);
                    removeLine(line, jTable1);
                    
                    logger.info("Classificação excluída com sucesso.");
                    JOptionPane.showMessageDialog(null, "Classificação excluída com sucesso!");
                } catch (final RegisterException ex) {
                    logger.error(ex);
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
}//GEN-LAST:event_btRemoveActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        logger.info("O botão btUpdate foi pressionado.");
        int line = jTable1.getSelectedRow();        
        if (line >= 0) {
            try {
                int id = Integer.parseInt(jTable1.getValueAt(line, 0).toString());
                Classification classification = facade.searchClassification(id);
                ClassificationDialog dialog = new ClassificationDialog(null, true, classification, 1);
                dialog.pack();
                dialog.setLocationRelativeTo(this);
                dialog.setVisible(true);
                removeLine(line, jTable1);
                addAllLinesTableClassification();
            } catch (final RegisterException ex) {
                logger.error(ex.getMessage());
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }            
        }
}//GEN-LAST:event_btUpdateActionPerformed

    private void btInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInsertActionPerformed
        logger.info("O botão btInsert foi pressionado.");
        ClassificationDialog dialog = new ClassificationDialog(null, true, null, 0);
        dialog.pack();
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);
        addAllLinesTableClassification();
}//GEN-LAST:event_btInsertActionPerformed

    protected void addLineTableClassification(Classification classification) {
        model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{classification.getId(), classification.getTechnique().toString(), classification, classification.isActive()});
    }

    public void addAllLinesTableClassification() {
        removeAllLinesTableClassification();
        for (Classification classification : facade.getProject().getClassifications()) {
            addLineTableClassification(classification);
        }
    }

    protected void updateLineTableClassification(String sentence, int line) {
        model = (DefaultTableModel) jTable1.getModel();
        model.setValueAt(sentence, line, 0);
    }

    private void removeAllLinesTableClassification() {
        model = (DefaultTableModel) jTable1.getModel();
        while (model.getRowCount() >= 1) {
            model.removeRow(0);
        }
    }

    private void removeLine(int line, JTable table) {
        model = (DefaultTableModel) table.getModel();
        model.removeRow(line);
    }

