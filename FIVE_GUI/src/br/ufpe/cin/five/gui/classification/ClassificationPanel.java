    private void btProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcessActionPerformed
        logger.info("O botão btProcess foi pressionado.");
        int line = jTable1.getSelectedRow();
        if (line >= 0) {
            Classification classification = (Classification) jTable1.getValueAt(line, 2);            
            ResultsDialog dialog = new ResultsDialog(null, true, classification);
            dialog.pack();
            dialog.setLocationRelativeTo(this);
            dialog.setVisible(true);
        }
        addAllLinesTableClassification();
}//GEN-LAST:event_btProcessActionPerformed

